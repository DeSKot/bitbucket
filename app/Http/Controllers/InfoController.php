     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $validated = $request->validate([
            'name' => 'max:50| required|min:5',
            'email' => 'email| required',
            'message' => 'required | max:500 | min:5'
        ]);

        $new_info = new Info;
        $new_info->name = $request->name;
        $new_info->email = $request->email;
        $new_info->message = $request->message;
        $new_info->save();

        return redirect()->back()->withSuccess('Категория была успешно добавлена!');
    }

